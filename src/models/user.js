import mongoose from "mongoose";
const Schema = mongoose.Schema;


const userSchema = new Schema({
    email: {
        type: String,
        unique: true,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now()
    }
});



export default mongoose.model('User', userSchema);